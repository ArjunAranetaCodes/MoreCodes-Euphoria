--If, ElseIf, and Else Structure

integer num1 = 1
integer num2 = 2

if num1 > num2 then
 puts(1, "First number is higher!\n")
end if

if num1 = num2 then
 puts(1, "They are equal!\n")
else
 puts(1, "They are not equal!\n")
end if

if num1 > num2 then
 puts(1, "First number is greater!\n")
elsif num1 < num2 then
 puts(1, "Second number is greater!\n")
end if



